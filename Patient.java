
public class Patient {
	 int id;
	    String name;
	    int age;
	    String gender;
	    String diagnosis;

	    public Patient(int id, String name, int age, String gender, String diagnosis) {
	        this.id = id;
	        this.name = name;
	        this.age = age;
	        this.gender = gender;
	        this.diagnosis = diagnosis;
	    }
}
